package ipctest;

import java.io.Serializable;

public class Person implements Serializable {
    String name;
    int age;

    public Person(String name) {
        
        this.name = name;
    }

    public Person(int age) {
        this.age = age;
    }

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
